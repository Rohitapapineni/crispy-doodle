def miletokm():
    mile=float(input('Enter distance in miles:'))
    km=mile*1.60934
    print('Distance in Kilometers is ',km,'KM')

def kmtomile():
    km=float(input('Enter distance in Kilometers:'))
    mile=km*0.621371
    print('Distance in Miles is ',mile,'Miles')

def feettoinches():
    feet=float(input('Enter length in feet:'))
    inch=feet*12
    print('Length in Inches is ',inch,'IN')

def inchestofeet():
    inch=float(input('Enter length in inches:'))
    feet=inch*0.0833333
    print('Length in Feet is ',feet,'ft')
